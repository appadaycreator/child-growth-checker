  function statsAt(sex, metric, ageM) {
    if (metric === 'kaup') {
      const h = statsAt(sex, 'height', ageM);
      const w = statsAt(sex, 'weight', ageM);
      const m2 = (h.mean / 100) * (h.mean / 100);
      return { mean: w.mean / m2, sd: w.sd / m2 };
    }
    const arr = DATA[sex][metric];
    const a = Math.max(arr[0][0], Math.min(arr[arr.length - 1][0], ageM));
    for (let i = 0; i < arr.length - 1; i++) {
      const [m0, mu0, sd0] = arr[i], [m1, mu1, sd1] = arr[i + 1];
      if (a >= m0 && a <= m1) {
        const t = m1 === m0 ? 0 : (a - m0) / (m1 - m0);
        return { mean: mu0 + (mu1 - mu0) * t, sd: sd0 + (sd1 - sd0) * t };
      }
    }
    const last = arr[arr.length - 1];
    return { mean: last[1], sd: last[2] };
  }

